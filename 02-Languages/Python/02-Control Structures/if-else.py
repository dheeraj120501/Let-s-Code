#Demonstration of conditional branching using if-else statements
#Refer: Conditional Branching.txt
#See: if-else.png

print("Conditional Branching Test Begins") #message
a = 11 #variable named a will get declared and assigned a value : 10
if a % 2 == 0: #criteria
    print(a, "is an even number")
else:
    print(a, "is an odd number")
print("Conditional Branching Test Ends")

#FYI:
# = is an assignment operator. It assigns the RHS value to the variable at LHS.
# == is a comparison operator. It compares the LHS and RHS values for equality and returns a boolean.
# % is a division operator. It returns the remainder of integer division.
